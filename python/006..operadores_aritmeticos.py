

# División exacta (/)
print(50 / 35) # División exacta 1.4285714285714286
print(round (50 / 35, 2)) # redondeado con 2 decimales 1.43

# División entera (/)
print(50 // 35)

regalos = 123
alumnos = 25

regalos_por_persona = regalos // alumnos

print(f'regalos por persona {regalos_por_persona}')

# Exponente (**)
print(2 ** 3) # 8
print(3 ** 3) # 27



