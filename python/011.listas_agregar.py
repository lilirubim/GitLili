
clothes = ['camisa', 'pantalón', 'abrigo', 'zapatos']
print(clothes)

# append() agrega un elemento al final de la lista

clothes.append('bufanda')
clothes.append('cinturón')

print(clothes)

# insert() agregar un elemento en una posición concreta
clothes.insert(2, 'reloj')
clothes.insert(0, 'gallumbos')
print(clothes)
